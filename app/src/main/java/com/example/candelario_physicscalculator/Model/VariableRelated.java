package com.example.candelario_physicscalculator.Model;

public class VariableRelated {
    double area_base_one, area_base_two, area_height, area_length, area_width, area_radius;
    double volume_radius, volume_height, volume_base;

    public VariableRelated() {}

    public double getArea_base_one() { return area_base_one; }
    public void setArea_base_one(double area_base_one) { this.area_base_one = area_base_one; }
    public double getArea_base_two() { return area_base_two; }
    public void setArea_base_two(double area_base_two) { this.area_base_two = area_base_two; }
    public double getArea_height() { return area_height; }
    public void setArea_height(double area_height) { this.area_height = area_height; }
    public double getArea_length() { return area_length; }
    public void setArea_length(double area_length) { this.area_length = area_length; }
    public double getArea_width() { return area_width; }
    public void setArea_width(double area_width) { this.area_width = area_width; }
    public double getArea_radius() { return area_radius; }
    public void setArea_radius(double area_radius) { this.area_radius = area_radius; }

    public double getVolume_radius() { return volume_radius; }
    public void setVolume_radius(double volume_radius) { this.volume_radius = volume_radius; }
    public double getVolume_height() { return volume_height; }
    public void setVolume_height(double volume_height) { this.volume_height = volume_height; }
    public double getVolume_base() { return volume_base; }
    public void setVolume_base(double volume_base) { this.volume_base = volume_base; }
}
